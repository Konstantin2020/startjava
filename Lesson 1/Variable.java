public class Variable {
    public static void main(String[] args) {
        byte coreCPU = 2;
        System.out.println("coreCPU = " + coreCPU);

        short ram = 8;
        System.out.println("ram = " + ram);

        int quantityHdd = 2;
        System.out.println("quantityHdd = " + quantityHdd);

        long totalHddCapacity = 627502022656l;
        System.out.println("totalHddCapacity = " + totalHddCapacity);

        float frequencyCPU = 2.60f;
        System.out.println("frequencyCPU = " + frequencyCPU);

        double monitorDiagonal = 15.6d;
        System.out.println("monitorDiagonal = " + monitorDiagonal);

        char letterIndexOfCPU = 'i';
        System.out.println("letterIndexOfCPU = " + letterIndexOfCPU);

        boolean mobile = true;
        System.out.println("mobile = " + mobile);
    }
}